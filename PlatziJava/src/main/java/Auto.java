public class Auto {
    private int clave;
    private int tipoAuto;
    private int tiempoLugar;
    private int costoLugar;

    public Auto(){
    
    }
    public int getClave() {
        return clave;
    }


    public void setClave(int clave) {
        this.clave = clave;
    }


    public int getTipoAuto() {
        return tipoAuto;
    }


    public void setTipoAuto(int tipoAuto) {
        this.tipoAuto = tipoAuto;
    }

    public int getTiempoLugar() {
        return tiempoLugar;
    }


    public void setTiempoLugar(int tiempoLugar) {
        this.tiempoLugar = tiempoLugar;
    }


    public int getCostoLugar() {
        return costoLugar;
    }


    public void setCostoLugar(int costoLugar) {
        this.costoLugar = costoLugar;
    }
}
